							<p><strong>Recipe Name: </strong>Spinach Lasagna</p>
							<p><strong>Ingredients:</strong></p>
								<ul class="indent">
									<li>1/4 cup olive oil</li>
									<li>1 small yellow onion, finely chopped</li>
									<li>2 teaspoons coarse salt</li>
									<li>2 pounds baby spinach</li>
									<li>4 tablespoons unsalted butter</li>
									<li>1/4 cup all purpose flour</li>
									<li>1 quart milk</li>
									<li>1 packed cup grated Parmesan (3 ounces)</li>
									<li>1/4 teaspoon freshly grated nutmeg</li>
									<li>1/4 teaspoon ground black pepper</li>
									<li>2 medium zucchini (about 1 pound), sliced lengthwise into 1/4-inch-thick planks</li>
									<li>8 ounces no-boil lasagna noodles</li>
									<li>6 ounces mozzarella, shredded</li>
									<li>1/3 packed cup (1 ounce) grated Pecorino Romano</li>

								</ul>
							
							<p><strong>Directions:</strong></p>
								<ol class="indent">
									<li>Preheat oven to 375 degrees with rack in middle position.</li><br />
									<li>Heat 1 tablespoon oil in a large pot over medium-high heat. When oil shimmers, add onions and 1/2 teaspoon salt and cook, stirring occasionally, until translucent, 3 minutes. Add spinach, a few handfuls at a time, and cook, stirring frequently, until all spinach is wilted, 3-5 minutes. Transfer to a mesh strainer and press against spinach to remove as much liquid as possible.</li><br />
									<li>In a medium saucepan, melt butter over high heat. Add flour and cook, whisking constantly, until roux is golden brown, 1 minute. Add milk and continue whisking until milk begins to boil and thickens enough to coat the back of a spoon. Remove from heat and stir in Parmesan, nutmeg, 1 teaspoon salt, and black pepper. Cover surface with plastic wrap.</li><br />
									<li>Heat remaining 3 tablespoons oil in a large skillet over medium-high heat and fry zucchini, turning once, until golden brown on both sides, 2 minutes per side. Transfer to paper towel-lined plate to drain and sprinkle with remaining 1/2 teaspoon salt.</li><br />
									<li>Spread 1 cup bechamel along the bottom of a 9- by 13-inch baking dish. Place one-third of noodles over bechamel in a single, overlapping layer. Top with 1 cup bechamel and half of spinach. Repeat with a second layer of noodles, bechamel, and spinach. Top with remaining noodles, remaining bechamel, zucchini, mozzarella, and Pecorino Romano. Cover with foil and bake 45 minutes. Remove foil and continue baking until cheese is golden in places and lasagna is bubbling along the edges, about 15 minutes.</li><br />
									<li>Remove lasagna from oven and let stand 20 minutes before slicing and serving.</li>
								</ol><br />
